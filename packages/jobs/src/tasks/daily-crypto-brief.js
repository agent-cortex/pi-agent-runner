import Parser from 'rss-parser';
import path from 'node:path';
import { writeText, ensureDir } from '../utils/fs.js';

const parser = new Parser();

const DEFAULT_SOURCES = [
  'https://www.coindesk.com/arc/outboundfeeds/rss/',
  'https://cointelegraph.com/rss',
  'https://www.theblock.co/rss.xml'
];

function pickTop(items, maxItems) {
  return items
    .sort((a, b) => new Date(b.isoDate || b.pubDate || 0) - new Date(a.isoDate || a.pubDate || 0))
    .slice(0, maxItems);
}

export async function runDailyCryptoBrief(input = {}, ctx = {}) {
  const maxItems = Number(input.maxItems || process.env.CRYPTO_MAX_ITEMS || 5);
  const sources = Array.isArray(input.sources) && input.sources.length ? input.sources : DEFAULT_SOURCES;

  const entries = [];
  for (const url of sources) {
    try {
      const feed = await parser.parseURL(url);
      (feed.items || []).slice(0, 12).forEach((item) => {
        if (!item?.link || !item?.title) return;
        entries.push({
          title: item.title,
          link: item.link,
          isoDate: item.isoDate || item.pubDate,
          source: feed.title || url,
        });
      });
    } catch {
      // Skip failing source, keep job resilient
    }
  }

  const top = pickTop(entries, maxItems);
  const date = new Date().toISOString().slice(0, 10);
  const dataDir = process.env.DATA_DIR || '/app/data/artifacts';
  await ensureDir(dataDir);
  const artifactPath = path.join(dataDir, `crypto-brief-${date}.md`);

  const lines = [
    `# Daily Crypto Brief — ${date}`,
    '',
    ...top.map((item, idx) => `${idx + 1}. [${item.title}](${item.link}) — ${item.source}`),
    '',
    `Generated by pi-agent-runner jobId=${ctx.jobId || 'n/a'}`,
  ];

  await writeText(artifactPath, lines.join('\n'));

  return {
    summary: `Generated ${top.length}-item crypto brief`,
    itemCount: top.length,
    items: top,
    artifactPath,
  };
}
